class TypeCast {
	public static void main(String[] args) {
		int x = 5;
		float y = 6.92f;
		double z = 222223323234433d;

		int xx = (int)z;
		float yy = x;
		double zz = y;

		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println("-------");
		System.out.println(xx);
		System.out.println(yy);
		System.out.println(zz);
	}
}